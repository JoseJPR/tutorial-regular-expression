console.group('/^(?:(?:\(?(?:00|\+)([1-4]\d\d|[1-9]\d?)\)?)?[\-\.\ \\\/]?)?((?:\(?\d{1,}\)?[\-\.\ \\\/]?){0,})(?:[\-\.\ \\\/]?(?:#|ext\.?|extension|x)[\-\.\ \\\/]?(\d+))?$/');
const exp01 = /^(?:(?:\(?(?:00|\+)([1-4]\d\d|[1-9]\d?)\)?)?[\-\.\ \\\/]?)?((?:\(?\d{1,}\)?[\-\.\ \\\/]?){0,})(?:[\-\.\ \\\/]?(?:#|ext\.?|extension|x)[\-\.\ \\\/]?(\d+))?$/;
console.log('625789077                ', exp01.test('625789077')) // true;
console.log('(+351) 282 43 50 50      ', exp01.test('(+351) 282 43 50 50')) // true;
console.log('1 (234) 567-8901         ', exp01.test('1 (234) 567-8901')) // true;
console.log('1-234 567.89/01 ext.1234 ', exp01.test('1-234 567.89/01 ext.1234')) // true;
console.log('(0055)(123)8575973       ', exp01.test('(0055)(123)8575973')) // true;
console.groupEnd('/^(?:(?:\(?(?:00|\+)([1-4]\d\d|[1-9]\d?)\)?)?[\-\.\ \\\/]?)?((?:\(?\d{1,}\)?[\-\.\ \\\/]?){0,})(?:[\-\.\ \\\/]?(?:#|ext\.?|extension|x)[\-\.\ \\\/]?(\d+))?$/');
